
public class foreach {

	public static void main(String[] args) {
		String[] isimler = {"Tolu","Batu","Cemo","Apo","Yusuf"};
		
		for(String eleman : isimler){
			System.out.println(eleman);
		}
		
	System.out.println("------------------------------------------");
		int[] sayilar = {1,2,3,4,5};
		
		for ( int a : sayilar) {
			System.out.println(a);
		}

	}

}
